// import modules
require('prototype.spawn')();
var roleHarvester = require('role.harvester');
var roleUpgrader = require('role.upgrader');
var roleBuilder = require('role.builder');
var roleRepairer = require('role.repairer');
var roleWallRepairer = require('role.wallRepairer');
var roleLongDistanceHarvester = require('role.longDistanceHarvester');
var roleDefender = require('role.defender');
var roleAttacker = require('role.attacker');

var HOME = 'W11N73';
var TARGET = 'W11N74';

module.exports.loop = function () {
    // check for memory entries of died creeps by iterating over Memory.creeps
    for (let name in Memory.creeps) {
        // and checking if the creep is still alive
        if (Game.creeps[name] == undefined) {
            // if not, delete the memory entry
            delete Memory.creeps[name];
        }
    }

    // for every creep name in Game.creeps
    for (let name in Game.creeps) {
        // get the creep object
        var creep = Game.creeps[name];

        // if creep is harvester, call harvester script
        if (creep.memory.role == 'harvester') {
            roleHarvester.run(creep);
        }
        // if creep is upgrader, call upgrader script
        else if (creep.memory.role == 'upgrader') {
            roleUpgrader.run(creep);
        }
        // if creep is builder, call builder script
        else if (creep.memory.role == 'builder') {
            roleBuilder.run(creep);
        }
        // if creep is repairer, call repairer script
        else if (creep.memory.role == 'repairer') {
            roleRepairer.run(creep);
        }
        // if creep is wallRepairer, call wallRepairer script
        else if (creep.memory.role == 'wallRepairer') {
            roleWallRepairer.run(creep);
        }
        // if creep is longDistanceHarvester, call longDistanceHarvester script
        else if (creep.memory.role == 'longDistanceHarvester') {
            roleLongDistanceHarvester.run(creep);
        }
            // if creep is defender, call defender script
        else if (creep.memory.role == 'defender') {
            roleDefender.run(creep);
        }
            // if creep is defender, call attacker script
        else if (creep.memory.role == 'attacker') {
            roleDefender.run(creep);
        }
    }

//    // find all towers
//    var towers = Game.rooms[HOME].find(FIND_STRUCTURES, {
//        filter: (s) => s.structureType == STRUCTURE_TOWER
//   });
    // for each tower
//    for (let tower of towers) {
//        // find closes hostile creep
//        var target = tower.pos.findClosestByRange(FIND_HOSTILE_CREEPS);
//       // if one is found...
//        if (target != undefined) {
//            // ...FIRE!
//            tower.attack(target);
//        }
//    }

    // setup some minimum numbers for different roles
    var minimumNumberOfHarvesters = 1;
    var minimumNumberOfUpgraders = 0;
    var minimumNumberOfBuilders = 0;
    var minimumNumberOfRepairers = 0;
    var minimumNumberOfWallRepairers = 0;
    var minimumNumberOfLongDistanceHarvestersW9N73 = 0;
    var minimumNumberOfLongDistanceHarvestersW2N4 = 0;
    var minimumNumberOfDefenders = 0;
    var minimumNumberOfAttackers = 1;

    // count the number of creeps alive for each role
    // _.sum will count the number of properties in Game.creeps filtered by the
    //  arrow function, which checks for the creep being a specific role
    var numberOfHarvesters = _.sum(Game.creeps, (c) => c.memory.role == 'harvester');
    var numberOfUpgraders = _.sum(Game.creeps, (c) => c.memory.role == 'upgrader');
    var numberOfBuilders = _.sum(Game.creeps, (c) => c.memory.role == 'builder');
    var numberOfRepairers = _.sum(Game.creeps, (c) => c.memory.role == 'repairer');
    var numberOfWallRepairers = _.sum(Game.creeps, (c) => c.memory.role == 'wallRepairer');
    var numberOfLongDistanceHarvestersW9N73 = _.sum(Game.creeps, (c) =>
        c.memory.role == 'longDistanceHarvester' && c.memory.target == 'W3N5');
    var numberOfLongDistanceHarvestersW2N4 = _.sum(Game.creeps, (c) =>
        c.memory.role == 'longDistanceHarvester' && c.memory.target == 'W2N4');
    var numberOfDefenders = _.sum(Game.creeps, (c) => c.memory.role == 'defender');
    var numberOfAttackers = _.sum(Game.creeps, (c) => c.memory.role == 'attacker');

    var energy = Game.spawns.Spawn1.room.energyCapacityAvailable;
    var name = undefined;

    // if not enough harvesters
    if (numberOfHarvesters < minimumNumberOfHarvesters) {
        // try to spawn one
        name = Game.spawns.Spawn1.createCustomCreep(energy, 'harvester');

        // if spawning failed and we have no harvesters left
        if (name == ERR_NOT_ENOUGH_ENERGY && numberOfHarvesters == 0) {
            // spawn one with what is available
            name = Game.spawns.Spawn1.createCustomCreep(
                Game.spawns.Spawn1.room.energyAvailable, 'harvester');
        }
    }
    // if not enough upgraders
    else if (numberOfUpgraders < minimumNumberOfUpgraders) {
        // try to spawn one
        name = Game.spawns.Spawn1.createCustomCreep(energy, 'upgrader');
    }
    // if not enough repairers
    else if (numberOfRepairers < minimumNumberOfRepairers) {
        // try to spawn one
        name = Game.spawns.Spawn1.createCustomCreep(energy, 'repairer');
    }
    // if not enough builders
    else if (numberOfBuilders < minimumNumberOfBuilders) {
        // try to spawn one
        name = Game.spawns.Spawn1.createCustomCreep(energy, 'builder');
    }
    // if not enough wallRepairers
    else if (numberOfWallRepairers < minimumNumberOfWallRepairers) {
        // try to spawn one
        name = Game.spawns.Spawn1.createCustomCreep(energy, 'wallRepairer');
    }
    // if not enough longDistanceHarvesters for W3N5
    else if (numberOfLongDistanceHarvestersW9N73 < minimumNumberOfLongDistanceHarvestersW9N73) {
        // try to spawn one
        name = Game.spawns.Spawn1.createLongDistanceHarvester(energy, 5, HOME, 'W9N73', 0);
    }
    // if not enough longDistanceHarvesters for W2N4
    else if (numberOfLongDistanceHarvestersW2N4 < minimumNumberOfLongDistanceHarvestersW2N4) {
        // try to spawn one
        name = Game.spawns.Spawn1.createLongDistanceHarvester(energy, 3, HOME, 'W2N4', 0);
    }
    else if (numberOfDefenders < minimumNumberOfDefenders) {
        // try to spawn one
        name = Game.spawns.Spawn1.createDefendCreep(energy, 'defender');
    }
    else if (numberOfAttackers < minimumNumberOfAttackers) {
        // try to spawn one
        name = Game.spawns.Spawn1.createAttackCreep(energy, 'attacker');
    }
    

    else {
        // else try to spawn a builder
        name = Game.spawns.Spawn1.createCustomCreep(energy, 'builder');
    }

    // print name to console if spawning was a success
    // name > 0 would not work since string > 0 returns false
    if (!(name < 0)) {
        console.log("Spawned new creep: " + name);
    }
};